// Current language
let currentLanguage = localStorage.getItem('petrovenca-lang') || 'es';

/**
 * Set the current language and update the page
 * @param {string} lang - Language code ('es' or 'en')
 */
function setLanguage(lang) {
    if (!translations[lang]) {
        console.error(`Language '${lang}' not supported`);
        return;
    }

    currentLanguage = lang;
    localStorage.setItem('petrovenca-lang', lang);
    document.documentElement.lang = lang;

    translatePage();
    updateLanguageSelector();
}

/**
 * Translate all elements with data-i18n attribute
 */
function translatePage() {
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = translations[currentLanguage][key];

        if (translation) {
            // Handle different element types
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (element.placeholder) {
                    element.placeholder = translation;
                }
            } else if (element.tagName === 'OPTION') {
                element.textContent = translation;
            } else {
                element.textContent = translation;
            }
        }
    });

    // Handle placeholders separately
    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = translations[currentLanguage][key];
        if (translation) {
            element.placeholder = translation;
        }
    });
}

/**
 * Update language selector button text
 */
function updateLanguageSelector() {
    const langText = document.querySelector('.lang-text');
    if (langText) {
        langText.textContent = currentLanguage.toUpperCase();
    }

    // Update active state in dropdown
    const langOptions = document.querySelectorAll('.lang-option');
    langOptions.forEach(option => {
        const lang = option.getAttribute('data-lang');
        if (lang === currentLanguage) {
            option.classList.add('active');
        } else {
            option.classList.remove('active');
        }
    });
}

/**
 * Initialize the language system
 */
function initLanguageSystem() {
    // Get saved language or detect from browser
    const savedLang = localStorage.getItem('petrovenca-lang');
    if (savedLang) {
        currentLanguage = savedLang;
    } else {
        // Detect browser language
        const browserLang = navigator.language.split('-')[0];
        currentLanguage = (browserLang === 'en') ? 'en' : 'es';
    }

    // Apply initial translation
    document.documentElement.lang = currentLanguage;
    translatePage();
    updateLanguageSelector();

    // Set up dropdown toggle
    const langToggle = document.getElementById('langToggle');
    const langDropdown = document.getElementById('langDropdown');

    if (langToggle && langDropdown) {
        langToggle.addEventListener('click', (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle('active');
        });

        // Close dropdown when clicking outside
        document.addEventListener('click', () => {
            langDropdown.classList.remove('active');
        });

        // Language option click handlers
        const langOptions = document.querySelectorAll('.lang-option');
        langOptions.forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.getAttribute('data-lang');
                setLanguage(lang);
                langDropdown.classList.remove('active');
            });
        });
    }
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', initLanguageSystem);
